-------------------------CASE OPERATOR ->  CONDITIONAL SEARCH -------------------------

SELECT ENAME,COMM,
CASE 
	WHEN COMM =0 THEN 'EARNING ZERO COMMISSION'
	WHEN COMM IS NULL THEN 'NOT EARNING COMMISSION'
	ELSE 'EARNING COMMISSION'
END AS SUMMERY
FROM EMP
ORDER BY COMM DESC

--IF COMM IS NULL SET TO 0
SELECT ENAME,
CASE 
	WHEN COMM IS NULL THEN '0.00'
	ELSE COMM
END
FROM EMP
ORDER BY COMM DESC


