 * The main goal is to give the developers the opportunity to add parallelism and/or concurrency to their applications. 
 * Moreover; the library gives the developer an idea of what's happening in the current task such as knowing the status, returned values, did the cancellation happened? , did an exception thrown and/or handled? and continuation. 
